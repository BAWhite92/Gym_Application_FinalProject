    private void informMouseClicked(MouseEvent evt, String name,
    String date, String time) throws Exception
    {
        String informedFriend = null;
        ArrayList<String> friends = new ArrayList<>();
        
        friends = gba.getFriends(UserName);
        if(friends.isEmpty()){
            JOptionPane.showMessageDialog(null, "No current friends to inform");
        }
        else{
            JComboBox cb = new JComboBox(friends.toArray());
            int input;
            input = JOptionPane.showConfirmDialog(this, cb, "Select Friend", 
                    JOptionPane.OK_CANCEL_OPTION);
            if(input == JOptionPane.OK_OPTION){
                String str = (String) cb.getSelectedItem();
                informedFriend = str;
                JOptionPane.showMessageDialog(null, "Your friend will be informed!");
                gba.informFriendClass(UserName, informedFriend, name, date, time);
            }
        }

    }
                    
                    
