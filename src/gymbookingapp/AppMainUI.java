    private void HomePageFriendsMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_HomePageFriendsMouseClicked
    {//GEN-HEADEREND:event_HomePageFriendsMouseClicked
        MemberHomePage.setVisible(false);
        MemberClasses.setVisible(false);
        MemberBookings.setVisible(false);
        MemberFriendsPage.setVisible(true);
        MemberAccount.setVisible(false);
        MemberEditAccInfo.setVisible(false);
        MemberHelp.setVisible(false);
         try
        {
            // TODO add your handling code here:
            FriendReqPanel.removeAll();
            ArrayList<String> friendRequesters;
            friendRequesters = gba.getFriendRequests(UserName);
            for(String s: friendRequesters){
                JPanel p = new JPanel();
                p.setName(s);
                var tem = new AppMainUI.friendRequests(p, s);
                //jPanel3.add(p);
                FriendReqPanel.revalidate();
            }
            
            MemberFriendsUpdateText.removeAll();
            ArrayList<String> friends;
            friends = gba.getFriends(UserName);
            for(String s: friends){
                JPanel p = new JPanel();
                p.setName(s);
                var tem = new AppMainUI.friendsList(p, s);
                MemberFriendsUpdateText.revalidate();
            }
            
                            
            //set friend class updates from friends who have 'informed' user of 
            //their class attendance
            MemberFriendsClassesText.removeAll();
            Map<String, Class> informed = gba.getFriendsInformed(UserName);
            for(Map.Entry<String,Class> entry : informed.entrySet()){
                JPanel p = new JPanel();
                Class c = entry.getValue();
                p.setName(entry.getKey());
                var tem = new AppMainUI.FriendUpdate(p, entry.getKey(), c.name, 
                        c.classDate.toString(), c.classTime.toString());
                MemberFriendsUpdateText.revalidate();
            }
            
            // set the 'top' friends achievements.
            topAchFriendsPanel.removeAll();
            ArrayList<String> friendNames = new ArrayList<>();
            friendNames = gba.getFriends(UserName);
            for(String s: friendNames){
               Member m = gba.getMemberInfo(s);
               JPanel p = new JPanel();
               p.setName(s);
               var tem = new AppMainUI.topAchFriends(p, s, m.achievementPoints);
               topAchFriendsPanel.revalidate();
            }
            
            Member self = gba.getMemberInfo(UserName);
            MemAchPoints.setText("Your Achievement Points :  " + 
                    self.achievementPoints);
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_HomePageFriendsMouseClicked


