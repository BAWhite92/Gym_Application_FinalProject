    private void MemberAccEditConfirmButtonMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_MemberAccEditConfirmButtonMouseClicked
    {//GEN-HEADEREND:event_MemberAccEditConfirmButtonMouseClicked
        try
        {
            SimpleDateFormat sdfD = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat sdfOrig = new SimpleDateFormat("dd-MM-yyyy");

            String name = MemberEditAccName.getText();
            String username = MemberEditAccUsername.getText();
            String dob = MemberEditAccDOB.getText();
            String email = MemberEditAccEmail.getText();
            String secAns = MemberEditAccSecAns.getText();

            gba.updateMember(UserName, name, username, dob, secAns, email);
            Member mCheck = gba.getMemberInfo(username);

            //check that the new data saved in database for trainer matches the data that was provided
            if(mCheck.name.equals(name) && mCheck.username.equals(username) && 
                    mCheck.dobString.equals(dob) && mCheck.email.equals(email) && 
                    mCheck.securityAnswer.equals(secAns))
                {
                    //return success message and update global username variable
                    UserName = username;
                    JOptionPane.showMessageDialog(null, "Information Updated "
                            + "Successfully");
                    MemberHomePage.setVisible(false);
                    MemberClasses.setVisible(false);
                    MemberBookings.setVisible(false);
                    MemberAccount.setVisible(true);
                    MemberEditAccInfo.setVisible(false);
                    MemberHelp.setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Update Failed, please "
                            + "check your input and try again");
                }

            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Update Failed, please check"
                        + " your input and try again");
                ex.printStackTrace();
            }

    }//GEN-LAST:event_MemberAccEditConfirmButtonMouseClicked

