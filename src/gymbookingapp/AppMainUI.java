    private void BookClassMouseClicked(MouseEvent evt, String name, String date, 
            String time)
    {
        SimpleDateFormat sdfD = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat sdfOrig = new SimpleDateFormat("dd-MM-yyyy");
        //create confirmation box
        JPanel panel = new JPanel();
        panel.setSize(new Dimension(290, 200));
        JLabel label1 = new JLabel("Are you sure you want to book on to this class?");
        label1.setVerticalAlignment(SwingConstants.BOTTOM);
        label1.setHorizontalAlignment(SwingConstants.CENTER);
        panel.add(label1);
        UIManager.put("OptionPane.minimumSize", new Dimension(300,100));
        int res = JOptionPane.showConfirmDialog(null, panel, "Remove Member",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        //act on confirm box
        if(res == 0){
            try
            {
                if(gba.bookingCheck(name, sdfD.format(sdfOrig.parse(date)), time, 
                        UserName)){
                    if(gba.bookClass(name, sdfD.format(sdfOrig.parse(date)), time, 
                            UserName)){
                    JOptionPane.showMessageDialog(null, "You are now scheduled "
                            + "to attend this class, enjoy!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "You current have "
                                + "too many strikes to join a class, "
                            + "please see a member of staff for more help.");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "You are already booked "
                            + "on to this class.");
                }
            
            HomePageClassesMouseClicked(evt);
            }
            catch (Exception ex)
            {
                ex.printStackTrace();
            }
        }
    }
    
