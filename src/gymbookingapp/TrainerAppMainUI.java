    private void TrainerAccEditConfirmButtonMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_TrainerAccEditConfirmButtonMouseClicked
    {//GEN-HEADEREND:event_TrainerAccEditConfirmButtonMouseClicked
        try
        {
            SimpleDateFormat sdfD = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat sdfOrig = new SimpleDateFormat("dd-MM-yyyy");
            
            String name = TrainerEditAccName.getText();
            String username = TrainerEditAccUsername.getText();
            String dob = TrainerEditAccDOB.getText();
            String email = TrainerEditAccEmail.getText();
            String secAns = TrainerEditAccSecAns.getText();
            
            gba.updateTrainer(Username, name, username, dob, secAns, email);
            Trainer tCheck = gba.getTrainerInfo(username);
            
            //check that the new data saved in database for trainer matches 
            //the data that was provided
            if(tCheck.name.equals(name) && tCheck.username.equals(username) 
                    && tCheck.dobString.equals(dob) && 
                    tCheck.email.equals(email) && 
                    tCheck.securityAnswer.equals(secAns)){
                //return success message and update global username variable
                Username = username;
                JOptionPane.showMessageDialog(null, "Information Updated "
                        + "Successfully");
                TrainerHomePage.setVisible(false);
                TrainerClassesPage.setVisible(false);
                TrainerEditClasses.setVisible(false);
                TrainerAddClass.setVisible(false);
                TrainerAccountPage.setVisible(true);
                TrainerEditAccInfo.setVisible(false);
                TrainerHelpPage.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Update Failed, please "
                        + "check your input and try again");
            }
            
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Update Failed, please check your "
                    + "input and try again");
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_TrainerAccEditConfirmButtonMouseClicked

