    private void EditClassMouseClicked(java.awt.event.MouseEvent evt, 
            String name, String date, String time) throws Exception
    {
        SimpleDateFormat sdfD = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat sdfOrig = new SimpleDateFormat("dd-MM-yyyy");
        //set view of the UI
        TrainerHomePage.setVisible(false);
        TrainerClassesPage.setVisible(false);
        TrainerEditClasses.setVisible(true);
        TrainerAddClass.setVisible(false);
        TrainerAccountPage.setVisible(false);
        TrainerEditAccInfo.setVisible(false);
        TrainerHelpPage.setVisible(false);
        //set up global variables to be used when update is initiated
        currentName = name;
        currentDate = sdfD.format(sdfOrig.parse(date));
        currentTime = time;
        //create class and use to set the textfields with current info
        Class c = gba.getClassInfo(currentName, currentDate, currentTime);
        String spaces = String.valueOf(c.spacesAvailable);
        EditClassName.setText(c.name);
        EditClassType.setSelectedItem(c.classType);
        EditClassSpaces.setSelectedItem(spaces);
        EditClassDate.setText(c.classDate.toString());
        EditClassTime.setText(c.classTime.toString());
        EditClassDescription.setText(c.classInfo);
        EditClassHeading.setText("Edit " + c.name + " class");
    }    
    
    private void RemoveClassMouseClicked(java.awt.event.MouseEvent evt, 
        String name, String date, String time) throws Exception
    {
        try{
        SimpleDateFormat sdfD = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat sdfOrig = new SimpleDateFormat("dd-MM-yyyy");
        currentName = name;
        currentDate = sdfD.format(sdfOrig.parse(date));
        currentTime = time;
        
        //create confirmation box
        JPanel panel = new JPanel();
        panel.setSize(new Dimension(290, 200));
        JLabel label1 = new JLabel("Are you sure you want to remove this "
                + "class?");
        label1.setVerticalAlignment(SwingConstants.BOTTOM);
        label1.setHorizontalAlignment(SwingConstants.CENTER);
        panel.add(label1);
        UIManager.put("OptionPane.minimumSize", new Dimension(300,100));
        int res = JOptionPane.showConfirmDialog(null, panel, "Remove Class",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        //act on confirm box
        if(res == 0){
            gba.removeClass(currentName, currentDate, currentTime);
        }
        else{
            
        }
        
        //ensure class have been removed and inform user
        Class c = gba.getClassInfo(currentName, currentDate, currentTime);
        if(c == null) {
            JOptionPane.showMessageDialog(null, "This class has now been "
                    + "cancelled.");
            TrainerHomePageClassesMouseClicked(evt);
        }

        } catch(Exception e){
            throw e;
        }
    }
   
